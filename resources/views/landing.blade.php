  <script>
    document.querySelectorAll('.group-3 .vector').forEach(vector => {
        vector.addEventListener('click', function() {
            var parent = this.closest('.group-3');
            parent.classList.toggle('active'); // Toggle kelas 'active' pada '.group-3'
            this.classList.toggle('rotate'); // Toggle kelas 'rotate' untuk memutar ikon

            var vectorWrapper = this.closest('.vector-wrapper');
            vectorWrapper.classList.toggle('active'); // Toggle kelas 'active' pada '.vector-wrapper'

            var frame = document.querySelector('.frame-wrapper');
            updateFrameHeight(frame);
        });
    });

    function updateFrameHeight(frame) {
        let totalHeight = 0;
        let groups = frame.querySelectorAll('.group-3');
        groups.forEach(group => {
            totalHeight += group.offsetHeight + 31; // Tambahkan 31px untuk 'gap' antar elemen
        });
        frame.style.height = `${totalHeight}px`; // Setel tinggi frame sesuai total tinggi elemen anak

        // Setelah menyesuaikan tinggi frame, update tinggi group-wrapper
        updateGroupWrapperHeight(frame);
    }

    function updateGroupWrapperHeight(frame) {
        let frameHeight = frame.offsetHeight + 200; // tambah 200px untuk padding atau margin ekstra
        let groupWrapper = document.querySelector('.group-wrapper');
        groupWrapper.style.height = `${frameHeight}px`;
    }

